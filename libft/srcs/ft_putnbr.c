#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar(45);
		if (n == -2147483648)
		{
			ft_putchar(2 + '0');
			n = n % 1000000000;
		}
		n = -n;
	}
	if ((n >= 0) && (n < 10))
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
