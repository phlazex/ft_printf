#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		n;
	int		pn;

	i = 0;
	while (((str[i] >= 0x09) && (str[i] <= 0x0D)) || (str[i] == 0x20))
		i++;
	pn = 1;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			pn = -pn;
		i++;
	}
	n = 0;
	while (((str[i] - 0x30) >= 0) && ((str[i] - 0x30) <= 9))
	{
		n = n * 10 + (str[i] - 0x30);
		i++;
	}
	return (n * pn);
}
