#include "libft.h"

double	ft_pow(double x, int y)
{
	double	n;
	int		i;

	n = x;
	if (!(y))
		return (1.0);
	else if (y > 0)
	{
		i = 1;
		while (i < y)
		{
			n *= x;
			i++;
		}
	}
	else
	{
		i = 0;
		while (i >= y)
		{
			n /= x;
			i--;
		}
	}
	return (n);
}
